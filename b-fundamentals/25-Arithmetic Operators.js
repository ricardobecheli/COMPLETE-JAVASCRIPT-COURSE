// Using destructuring:

let [a, b, c, d] = [7, 15, 21, 48000] // values can be numbers or any other type.
const soma = (a + b + c + d)
console.log(soma)

const divisao = (a % b) + (c / d) // const!
console.log(divisao)

let contas = ((d/c/b/a)/3)
console.log(contas)

let [e, f, g, h] = [3*7, 2*2, 4*8, 25/2] // values can be operations or any other type.
const plus = (e+f+g+h)
console.log(plus)

let minus = (e-f-g-h)*(e-f-g-h)
console.log(minus)



let idade = 31;
console.log(typeof 31) // type = number
console.log(typeof idade) // type = number

let salario = 7538.32
console.log(typeof salario) // type = number
// the output is "number" for all of them. 
// JS does not make difference between integer and other types of numbers.

let estaChovendo = true // or false : type = boolean
console.log(estaChovendo)
console.log(typeof estaChovendo)

let estaFrio = false
console.log(estaFrio)
console.log(typeof estaFrio)

console.log(typeof "text test") // not necessary to declare the variable before typeof.
// It's a string.

// THESE 3 ARE THE 3 BASIC TYPES IN JS.

